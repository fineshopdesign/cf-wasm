export const entitiesMap = Object.keys(entitiesDict).reduce(
	(accumulator, ascii) => {
		const array = entitiesDict[ascii];
		array.forEach((dict) =>
			dict.forEach((entity) => {
				if (entity.trim().length !== 0) {
					accumulator[entity] = ascii;
				}
			})
		);
		return accumulator;
	},
	{} as Record<string, string>
);

export const entities = Object.keys(entitiesMap);

/**
 * Replaces all entities in a string
 *
 * @param string The input string
 *
 * @returns A string with replaced entities
 */
export const replaceEntities = (string: string) => {
	let result = string;
	entities.forEach((entity) => {
		result = result.replaceAll(entity, entitiesMap[entity]);
	});
	return result;
};
