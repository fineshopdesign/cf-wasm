export const ENTITIES_MAP = Object.keys(ENTITIES_DICT).reduce(
  (accumulator, ascii) => {
    const array = ENTITIES_DICT[ascii];
    for (const dict of array) {
      for (const entity of dict) {
        if (entity.trim().length !== 0) {
          accumulator[entity] = ascii;
        }
      }
    }
    return accumulator;
  },
  {} as Record<string, string>,
);

export const ENTITIES = Object.keys(ENTITIES_MAP);

/**
 * Replaces all entities in a string
 *
 * @param string The input string
 *
 * @returns A string with replaced entities
 */
export const replaceEntities = (string: string) => {
  let result = string;
  for (const entity of ENTITIES) {
    result = result.replaceAll(entity, ENTITIES_MAP[entity]);
  }
  return result;
};
